package application;



public class Blog {

private String title;

private String body;

	public Blog() {

	setTitle(title);

	setBody(body);

}

	public String getBody() {

		return body;

	}

	public void setBody(String body) {

		this.body = body;

	}

	public String getTitle() {

		return title;

	}

	public void setTitle(String title) {

		this.title = title;

	}

}